	<h3>One of SVGs leading artists signs international deal</h3>
	
<p>Local Creative Calvert Jones continues to push the boundaries and his latest move has seen him sign with Elizabeth Sloane, a business development and deal origination firm with operations in the Caribbean and West Africa.  

Jones is known locally, regionally and internationally through his brand of art “Tropical Realism”.  

He is a self-taught visual artist and entrepreneur who in November 2016 gifted Prince Harry with a portrait and most recently this week, Jamaican superstar Andrae “Popcaan” Sutherland received a piece from Jones depicting the dancehall artiste and his mother whom he fondly refers to as “Miss Rhona”.  

On Monday, Jones, said he signed a deal with Elizabeth Sloane that will assist him with Business Development Services specifically as it relates to Intellectual Property (IP) syndication.  

Jones said the company has worked with names like UNESCO, British Council and Mastercard and while it is a boutique firm, Elizabeth Sloane is an extremely professional organisation with a global reputation.  
	
He said he first learnt of Elizabeth Sloane when he was contacted by Melanie Wynter, Managing Director of Elizabeth Sloane to purchase a print “of my most prized painting” which is dubbed “The Last Colonial Meal”.
	
Calvert Jones is an entrepreneur and self-taught Visual artist. His professional career spans photography, videography, graphic design, interior design, and fine art.  He also serves as a Director of Invest SVG.  

Calvert is the son of Sandra and Sidney Jones. He grew up in the rural village of Cedars, St. Vincent and the Grenadines, this meant a forty-minute daily commute to capital Kingstown for school. These trips cemented in his mind the imagery necessary to stimulate his creative potential. He sold his first piece of artwork at the age of twelve.  
	</p>

